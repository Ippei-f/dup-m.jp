//関数
function VOICE_TEXT($str,$ratio=''){
	/*
			[0]:タイトル
			[1]:文章

			※文章の中に以下の自作タグを入れることでタイトルタグや画像を挿入できる
			
			[[タイトル]]...[[]]内に文字を入れることでそこがタイトルになる
			
			[L]..........PCの時は左寄りに表示・スマホの時も表示
			[R]..........PCの時は右寄りに表示・スマホの時も表示
			[L-pc].......PCの時のみ表示（左寄り）
			[R-pc].......PCの時のみ表示（右寄り）
			[sp].........スマホの時のみ表示
			
			[L-text:（注釈）:]...左寄り画像の注釈
			[R-text:（注釈）:]...右寄り画像の注釈
	*/
	global $casenum;
	global $photo_num;
	$dir='../images/content/voice/detail/'.$casenum.'/';
	$plus=0;
	if(strpos($str,'[L+1]')!==false){$plus++;}
	if(strpos($str,'[R+1]')!==false){$plus++;}
	$str=str_replace(array("[[　","[[ ","[["),'<h4 class="font_meiryo">',$str);
	$str=str_replace(array("　]]"," ]]","]]"),'</h4>',$str);
	$str=str_replace(array("[L]"),'<img src="'.$dir.'p'.sprintf('%02d',$photo_num).'.jpg" class="dpB floatL_pc">',$str);
	$str=str_replace(array("[R]"),'<img src="'.$dir.'p'.sprintf('%02d',$photo_num).'.jpg" class="dpB floatR_pc">',$str);
	$str=str_replace(array("[L+1]"),'<img src="'.$dir.'p'.sprintf('%02d',$photo_num+1).'.jpg" class="dpB floatL_pc clearL">',$str);
	$str=str_replace(array("[R+1]"),'<img src="'.$dir.'p'.sprintf('%02d',$photo_num+1).'.jpg" class="dpB floatR_pc clearR">',$str);
	$str=str_replace(array("[L-pc]"),'<img src="'.$dir.'p'.sprintf('%02d',$photo_num).'.jpg" class="dpB floatL_pc sp_vanish">',$str);
	$str=str_replace(array("[R-pc]"),'<img src="'.$dir.'p'.sprintf('%02d',$photo_num).'.jpg" class="dpB floatR_pc sp_vanish">',$str);
	$str=str_replace(array("[sp]"),'<img src="'.$dir.'p'.sprintf('%02d',$photo_num).'.jpg" class="dpB pc_vanish">',$str);
	$str=str_replace(array("[L-text:"),'<div class="dpB floatL_pc nomgn clearL">',$str);
	$str=str_replace(array("[R-text:"),'<div class="dpB floatR_pc nomgn clearR">',$str);
	$str=str_replace(array(":]"),'</div>',$str);
	$str=str_replace(array("\r\n","\n","\r"),'<br>',$str);
	$str=str_replace(array("</h4><br>"),'</h4>
',$str);
	
	echo '<li'.(($ratio!='')?' class="'.$ratio.'"':'').'>'.$str.'
<div class="clear"></div></li>';

	//画像が1枚でも使われていたら（<img>タグがあれば）画像番号カウント
	if(strpos($str,'<img')!==false){
		$photo_num++;
		$photo_num+=$plus;
	}

}

